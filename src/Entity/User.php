<?php

namespace App\Entity;

use App\Repository\UserRepository;
use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Security\Core\User\UserInterface;
use Symfony\Component\Validator\Constraints as Assert;

/**
 * @ORM\Table(name="im2021_user")
 * @ORM\Entity(repositoryClass=UserRepository::class)
 */
class User implements UserInterface
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer", name="pk")
     */
    private $id;


    /**
     * @ORM\Column(type="string", length=30, options={"default"=true})
     */
    private $nom;

    /**
     * @ORM\Column(type="string", length=30, options={"default"=true})
     */
    private $prenom;

    /**
     * @ORM\Column(type="string", length=64, unique=true, options={"comment"="Set de login doit Ãªtre unique"})
     */
    private $username;

    /**
     * @ORM\Column(type="json")
     */
